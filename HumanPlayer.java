package Connect4;

import java.awt.Color;

public class HumanPlayer extends Player
{

    /**
     * Constructor: Creates a HumanPlayer object
     * 
     * @param n The name of the player
     * @param c The color of the player
     */
    public HumanPlayer(String n, Color c)
    {
        name = n;
        color = c;
    }

}
